#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*p;
	size_t				i;

	p = (const unsigned char *)s;
	i = 0;
	while (i < n && p[i] != (unsigned char)c)
		i++;
	if (i < n)
		return ((void *) &p[i]);
	return (NULL);
}
